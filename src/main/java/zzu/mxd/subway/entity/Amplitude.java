package zzu.mxd.subway.entity;

public class Amplitude {
    double x;//x轴
    double y;//y轴

    public Amplitude(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }
}
